    private void botaoCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCriarActionPerformed
        CasoDAO casoDAO = new CasoDAO();

        if(casoDAO.verificaNomeCaso(campoDescricao.getText())){
            JOptionPane.showMessageDialog(this, "Já existe um caso com a descrição informada!");
        }
        else if(verificaCamposPreenchidos()){
            preencheObjetoCaso();
           
            if(criaPastaCaso()){
                if(casoDAO.inserir(novoCaso)){
                    casoDAO.relacionaCasoUsuario(Acesso.Criador, usuario.getId(), 0L); // usuario criador
                    
                    if(!usuariosSelecionados.isEmpty()){
                        for(Usuario usuarioSelecionado : usuariosSelecionados){
                            casoDAO.relacionaCasoUsuario(Acesso.Visualizar, usuarioSelecionado.getId(), 0L);
                        }
                    }
                    JOptionPane.showMessageDialog(this, "Caso criado!");
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_botaoCriarActionPerformed

