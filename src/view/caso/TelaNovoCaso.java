    private void carregaJListContatos(){
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        usuarios = usuarioDAO.pesquisaUsuarios(usuario.getId());
        jListUsuarios.setModel(new JListModel(usuarios));
    }
    
    private boolean verificaCamposPreenchidos(){
        if(campoDescricao.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo DESCRIÇÃO não preenchido!");
            return false;
        }
        if(campoNomeInvestigado.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo NOME DO INVESTIGADO não preenchido!");
            return false;
        }
        return true;
    }
        
    private void preencheObjetoCaso(){
        novoCaso = new Caso();
        novoCaso.setDescricao(campoDescricao.getText());
        novoCaso.setNome_investigado(campoNomeInvestigado.getText());
        novoCaso.setUsuario(usuario);
        novoCaso.setUsuarios_acesso(usuariosSelecionados);
    }
    
    private boolean criaPastaCaso(){
        File arquivoNovo = new File(usuario.getDiretorio_casos()+"\\"+novoCaso.getDescricao());
        arquivoNovo.mkdir();
        
        if(!arquivoNovo.exists()){
            JOptionPane.showMessageDialog(this, "Erro ao criar pasta do caso!");
            return false;
        }
        
        novoCaso.setDiretorio(arquivoNovo.getAbsolutePath());
        return true;
    }
    
