    private void botaoAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAbrirActionPerformed
        if(dispositivoSelecionado == null){
            JOptionPane.showMessageDialog(this, "Selecione um dispositivo!");
        }
        else{
            DispositivoDAO dispositivoDAO = new DispositivoDAO();
            
            if(dispositivoDAO.verificaImportacaoDispositivo(dispositivoSelecionado.getTabela_contato()) > 0){
                new TelaPrincipalDispositivo(this, false, dispositivoSelecionado).setVisible(true);
            }
            else{
                new TelaDispositivoImportaBanco(dispositivoSelecionado).setVisible(true);
            }
        }
    }//GEN-LAST:event_botaoAbrirActionPerformed

    private void arvoreDispositivosValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_arvoreDispositivosValueChanged
        if(!evt.getPath().getLastPathComponent().toString().equals(casoSelecionado.getDescricao())){
            DispositivoDAO dispositivoDAO = new DispositivoDAO();
            dispositivoSelecionado = dispositivoDAO.pesquisaDispositivoPorNome(evt.getPath().getLastPathComponent().toString(), casoSelecionado);
        }
    }//GEN-LAST:event_arvoreDispositivosValueChanged

    private void tabelaMeusCasosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMeusCasosMouseClicked
        if(evt.getClickCount() > 1){
            this.casoSelecionado = casos.get(tabelaMeusCasos.getSelectedRow());
            carregaListaDispositivos();
            carregaJTreeDispositivos();
            arvoreDispositivos.setVisible(true);
        }
        
        else if(evt.getButton() == evt.BUTTON3){
            tabelaMeusCasos.setRowSelectionInterval(tabelaMeusCasos.rowAtPoint(evt.getPoint()), tabelaMeusCasos.rowAtPoint(evt.getPoint()));
            casoSelecionado = casos.get(tabelaMeusCasos.rowAtPoint(evt.getPoint()));
            carregaMenu();
            menu.show(tabelaMeusCasos, evt.getX(), evt.getY());
            menu.setVisible(true);
            menu.setLocation(evt.getLocationOnScreen());
        }
    }//GEN-LAST:event_tabelaMeusCasosMouseClicked

