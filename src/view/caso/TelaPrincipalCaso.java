    private void carregaJTreeDispositivos(){
        DefaultMutableTreeNode titulo = new DefaultMutableTreeNode(casoSelecionado.getDescricao());

        if(dispositivos.isEmpty()){
            DefaultMutableTreeNode dispositivo[] = new DefaultMutableTreeNode[1];
            dispositivo[0] = new DefaultMutableTreeNode("Sem dispositivos");
            titulo.add(dispositivo[0]);
        }
        else{
            for (int i = 0; i < dispositivos.size(); i++) {
                DefaultMutableTreeNode dispositivo[] = new DefaultMutableTreeNode[dispositivos.size()];
                dispositivo[i] = new DefaultMutableTreeNode(dispositivos.get(i));
                titulo.add(dispositivo[i]);
            }
        }
        
        arvoreDispositivos.setModel(new DefaultTreeModel(titulo));
    }
    
    
    private void preencheTabelasCasos(){
        CasoDAO casoDAO = new CasoDAO();
        casos = casoDAO.pesquisaListaCasos(usuarioLogado, Acesso.Criador);
        casosCompartilhados = casoDAO.pesquisaListaCasos(usuarioLogado, Acesso.Visualizar);
        tabelaMeusCasos.setModel(new TabelaModeloCaso(casos));
        tabelaCasosCompartilhados.setModel(new TabelaModeloCaso(casosCompartilhados));
    }
    
    
    private void carregaListaDispositivos(){
        DispositivoDAO dispositivoDAO = new DispositivoDAO();
        dispositivos = dispositivoDAO.dispositivosPorCaso(casoSelecionado);
    }
    
    
    private void alterarCaso(){
        if(casoSelecionado == null){
            JOptionPane.showMessageDialog(this, "Selecione um caso!");
        }
        else{
            System.out.println(casoSelecionado.getId());
            new TelaAlterarCaso(this, true, casoSelecionado, usuarioLogado).setVisible(true);
        }
    }
    
    private void excluirCaso(){
        if(casoSelecionado == null){
            JOptionPane.showMessageDialog(this, "Selecione um caso!");
        }
        else{
            int resposta = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir o caso: "+casoSelecionado.getDescricao(), "EXCLUIR CASO", JOptionPane.YES_NO_CANCEL_OPTION);
            
            if(resposta == 0){
                DispositivoDAO dispositivoDAO = new DispositivoDAO();
                List<Dispositivo> dispositivosExcluir = dispositivoDAO.dispositivosPorCaso(casoSelecionado);
                
                for(Dispositivo dispositivo : dispositivosExcluir){
                    dispositivoDAO.excluir(dispositivo);
                    File file = new File(dispositivo.getDiretorio());
                    file.delete();
                }
                
                CasoDAO casoDAO = new CasoDAO();
                if(casoDAO.excluir(casoSelecionado)){
                    File file = new File(casoSelecionado.getDiretorio());
                    file.delete();
                    
                    casoSelecionado = null;  
                    JOptionPane.showMessageDialog(this, "Caso exclu√≠do!");
                }
            }
        }
    }
    
    
    
    private void carregaMenu(){
        menu = new JPopupMenu();
        itemAlterarCaso = new JMenuItem("Alterar");
        itemExcluirCaso = new JMenuItem("Excluir");
        menu.add(itemAlterar);
        menu.add(itemExcluir);
        tabelaMeusCasos.add(menu);
        
        itemAlterarCaso.addActionListener(new ActionListener(){ 
            @Override
            public void actionPerformed(ActionEvent e) {
                alterarCaso();
            }
        });
        
        itemExcluirCaso.addActionListener(new ActionListener(){ 
            @Override
            public void actionPerformed(ActionEvent e) {
                excluirCaso();
            }
        });
    }
    
    
    
    
