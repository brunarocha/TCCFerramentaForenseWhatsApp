    private void preencheCampos(){
        campoUsuario.setText(acessoBanco.getUsuario());
        campoSenha.setText(acessoBanco.getSenha());
        campoHost.setText(acessoBanco.getHost());
        campoPorta.setText(acessoBanco.getPorta());
        campoBanco.setText(acessoBanco.getBanco());
    }
    
    
    private void carregaObjeto(){
        acessoBanco.setUsuario(campoUsuario.getText());
        acessoBanco.setSenha(String.valueOf(campoSenha.getPassword()));
        acessoBanco.setHost(campoHost.getText());
        acessoBanco.setPorta(campoPorta.getText());
        acessoBanco.setBanco(campoBanco.getText());
    }
    
    
    

